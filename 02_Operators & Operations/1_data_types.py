an_integer = 12
some_string = "Python"
float_value = 34.7
is_true = True
is_none = None

# you can then print data types with built-in type() function
print(an_integer, type(an_integer))     # int
print(some_string, type(some_string))   # str
print(float_value, type(float_value))   # float
print(is_true, type(is_true))           # bool
print(is_none, type(is_none))           # NoneType